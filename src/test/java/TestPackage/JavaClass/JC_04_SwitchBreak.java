package JavaPackage;

public class JC_04_SwitchBreak {

	public static void main(String[] args) {
		
		
		int score=80;
		
		switch (score) {
		
		case 100:
			
			
			System.out.println("Distinction");
			
			break;
			
		case 90:
			
			
		System.out.println("Very Good");
		
		break;
		
		default:
			
			System.out.println("Grade not defined");
		
		
		
		}
		
	
}

}